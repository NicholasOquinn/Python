#Get number within range of 1 and 7
Day_of_week = int(input('Enter a number between 1 and 7'))

#Create if statement that displays corresponding
#day of the week depending on input.
if Day_of_week >= 1 and Day_of_week <= 7:
    if Day_of_week == 1:
        print('Monday')
    if Day_of_week == 2:
        print('Tuesday')
    if Day_of_week == 3:
        print('Wednesday')
    if Day_of_week == 4:
        print('Thursday')
    if Day_of_week == 5:
        print('Friday')
    if Day_of_week == 6:
        print('Saturday')
    if Day_of_week == 7:
        print('Sunday')

